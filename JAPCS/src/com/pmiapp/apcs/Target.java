    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (tpfChooser.showOpenDialog(this)==JFileChooser.APPROVE_OPTION) {
            try {
                targetFile=tpfChooser.getSelectedFile().getPath();
                BufferedReader in = new BufferedReader(
                        new FileReader(tpfChooser.getSelectedFile()));
                clear();
                try {
                    for (int i=0; i<50; i++) {
                        String s = in.readLine();
                        if (s==null) break;
                        String[] vals = s.split(" ",3);
                        if (vals.length<3) break;
                        Float f0 = new Float(vals[0]);
                        Float f1 = new Float(vals[1]);
                        Float f2 = new Float(vals[2]);
                        // original vb6 program terminated reading
                        // if the first value was 0
                        if (f0==0) break;
                        jTable1.getModel().setValueAt(f0,i,1);
                        jTable1.getModel().setValueAt(f1,i,2);
                        jTable1.getModel().setValueAt(f2,i,3);
                    }
                }
                catch (NumberFormatException e3) {
                    JOptionPane.showMessageDialog(this,"Improper number format");
                }
                in.close();
            }
            catch (FileNotFoundException e) {
                JOptionPane.showMessageDialog(this,"File Not Found");
            }
            catch (IOException e2) {
                JOptionPane.showMessageDialog(this,"Error reading file");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (tpfChooser.showSaveDialog(this)==JFileChooser.APPROVE_OPTION) {
            File f = tpfChooser.getSelectedFile();
            // make sure extension is ".tpf"
            // note that getExtension always returns a lower case string
            if (!com.pmiapp.common.TxtFilter.getExtension(f).equals("tpf")) {
                String s=f.getName();
                if (s.endsWith(".")==false)
                    s=s+".";
                f=new File(f.getParentFile(),s+"tpf");
            }
            try {
                targetFile=f.getPath();
                PrintWriter out = new PrintWriter(f);
                for (int i=0; i<50; i++) {
                    out.print((Float)jTable1.getModel().getValueAt(i,1));
                    out.print(" ");
                    Float ff = (Float)jTable1.getModel().getValueAt(i,2);
                    out.print(ff);
                    out.print(" ");
                    ff = (Float)jTable1.getModel().getValueAt(i,3);
                    out.println(ff);
                    
                    // original vb6 program terminated output after second value was 0
                    if (ff==0) break;
                }
                out.close();
                if (out.checkError())
                    JOptionPane.showMessageDialog(this,"Error writing to file");
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(this,"Error opening file for writing");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

