    public void BuildColumn(int column, int startVal, int incVal, int totalVal)
    {
        int lastValue = startVal;
        for(int i = 0; i < totalVal; i++)
            {
                java.lang.Float f0 = new java.lang.Float(lastValue);
                jTable1.getModel().setValueAt(f0,i,column);
                lastValue += incVal;
            }
    }
    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int increment = 0;
        int startValue = 0;
        int totalValues = 0;
         try
            {
                increment = Integer.parseInt(jTextField2.getText());
                
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(this, "Please input a whole number for a starting value.");
                return;
            }
            try
            {
                startValue= Integer.parseInt(jTextField1.getText());
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(this, "Please input a whole number for an increment.");
                return;
            }
            try
            {
                totalValues = Integer.parseInt(jTextField3.getText());
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(this, "Please input a whole number for a total number of values.");
                return;
            }
            if (totalValues > 50)
            {
                JOptionPane.showMessageDialog(this, "Please input a whole number of 50 or less for a total number of values.");
                return;
            }
        if (jRadioButton1.isSelected())
        {
            //check to make sure everything is kosher cooking.
           
            //if everything is cool, clear any old values first
            ClearColumn(1);
            BuildColumn(1, startValue, increment, totalValues);
            
        }
        if (jRadioButton2.isSelected())
        {
            
            ClearColumn(2);
            BuildColumn(2, startValue, increment, totalValues);
           
        }
        if (jRadioButton3.isSelected())
        {
            ClearColumn(2);
            BuildColumn(2, startValue * 60, increment * 60, totalValues);
        }
        if (jRadioButton4.isSelected())
        {
            //build temperature column
            ClearColumn(3);
            BuildColumn(3, startValue, increment, totalValues);
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

